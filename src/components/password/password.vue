<template>
    <confirm :title="pwdTitle"
             :content="content" :showMask="showMask"
             :btnUnuse="isLoading" :isShowLoading="true"
             :leftBtnTxt="cancelTxt || $t('btn.cancel')" :rightBtnTxt="submitTxt || $t('btn.submit')"
             :leftBtnClick="exchange ? _submit : _cancle"  :rightBtnClick="exchange ? _cancle : _submit">

        <form autocomplete="off" v-show="isShowPWD" class="pass-input" :class="{ 'distance': !!content }">
            <input ref="passInput" v-model="password" :placeholder="$t('pwdConfirm.placeholder')" type="password"/>
        </form>

        <hold-pwd-view v-show="isShowPWD && isShowHold"></hold-pwd-view>
    </confirm>
</template>

<script>
import { StatusMap } from 'wallet';
import localStorage from 'utils/store';
import confirm from 'components/confirm.vue';
import holdPwdView from './holdPwd.vue';

let lastE = null;
const HoldPwdKey = 'isHoldPWD';
const ShowHoldNumKey = 'showHoldPWDNum';

export default {
    components: { confirm, holdPwdView },
    props: {
        type: {
            type: String,
            default: 'normal'
        },
        showMask: {
            type: Boolean,
            default: true
        },
        title: {
            type: String,
            default: ''
        },
        cancel: {
            type: Function,
            default: () => {}
        },
        submit: {
            type: Function,
            default: () => {}
        },
        content: {
            type: String,
            default: ''
        },
        submitTxt: {
            type: String,
            default: ''
        },
        cancelTxt: {
            type: String,
            default: ''
        },
        isShowPWD: {
            type: Boolean,
            default: true
        },
        exchange: {
            type: Boolean,
            default: false
        }
    },
    mounted() {
        lastE = this.$onKeyDown(13, () => {
            this._submit();
        });
    },
    destroyed() {
        this.$onKeyDown(13, lastE);
    },
    data() {
        const isHoldPWD = !!localStorage.getItem(HoldPwdKey);
        const showHoldNum = +localStorage.getItem(ShowHoldNumKey) || 0;
        const isShowHold = showHoldNum < 3 && !isHoldPWD;

        localStorage.setItem(ShowHoldNumKey, isShowHold ? showHoldNum + 1 : 4);

        return {
            password: '',
            isLoading: false,
            isShowHold
        };
    },
    computed: {
        pwdTitle() {
            if (this.type === 'normal') {
                return this.title || this.$t('pwdConfirm.title');
            }

            const name = this.$store.state.wallet.name;
            return this.$t('pwdConfirm.unlockAcc', { name });
        },
        currHDAcc() {
            return this.$store.state.wallet.currHDAcc;
        },
        isLogin() {
            return this.$store.state.wallet.status === StatusMap.UNLOCK;
        }
    },
    methods: {
        clear() {
            this.password = '';
        },

        _cancle() {
            this.clear();
            this.cancel && this.cancel();
        },
        _submit() {
            if (this.isLoading) {
                return;
            }

            if (!this.isShowPWD) {
                this.clear();
                this.submit && this.submit();
                return;
            }

            const password = this.$trim(this.password);
            if (!password) {
                this.$toast(this.$t('hint.pwEmpty'));
                return false;
            }

            if (!this.currHDAcc) {
                this.$toast(this.$t('hint.err'));
                return false;
            }

            const deal = result => {
                if (!result) {
                    this.$toast(this.$t('hint.pwErr'));
                    return false;
                }

                if (this.type !== 'normal') {
                    this.$toast(this.$t('unlockSuccess'));
                }

                this.clear();
                this.submit && this.submit();
            };

            if (this.isLogin) {
                this.currHDAcc.verify(password).then(() => {
                    deal(true);
                }).catch(() => {
                    deal(false);
                });
                return;
            }

            this.isLoading = true;
            this.$store.commit('login', password).then(() => {
                this.isLoading = false;
                if (!this.password) {
                    return;
                }
                this.currHDAcc.activate();
                deal(true);
            }).catch(err => {
                console.warn(err);
                this.isLoading = false;
                if (!this.password) {
                    return;
                }
                console.warn(err);
                deal(false);
            });
        }
    }
};
</script>

